export function getInitialLanguage() {
  if (typeof window === 'undefined') return 'en';
  const stored = window.localStorage.getItem('forecast-language');
  if (stored && SUPPORTED_LANGUAGES.includes(stored)) return stored;
  const browser = window.navigator.language.slice(0, 2);
  return SUPPORTED_LANGUAGES.includes(browser) ? browser : 'en';
}

export function translate(language, key, fallback = null, params = {}) {
  const locale = translations[language] || translations.en;
  const raw = locale[key] || fallback || translations.en[key] || key;
  return String(raw).replace(/\{(\w+)\}/g, (_, name) => params[name] ?? `{${name}}`);
}
